/// Error type for NFS operations.
#[derive(Debug, Snafu)]
#[snafu(visibility(pub))]
pub enum Error {
    /// Failed to initialize NFS context.
    #[snafu(display("Failed to initialize NFS context"))]
    ContextInitFailed,

    /// NFS operation failed with an error from the server.
    #[snafu(display("NFS operation failed: {message}"))]
    NfsOperationFailed { message: String },

    /// Path contains invalid characters (interior NUL byte).
    #[snafu(display("Invalid path: contains NUL byte"))]
    InvalidPath { source: NulError },

    /// Unknown or unsupported file type encountered.
    #[snafu(display("Unknown file type: {file_type}"))]
    UnknownFileType { file_type: u32 },
}

/// Result type for NFS operations.
pub type Result<T, E = Error> = std::result::Result<T, E>;

/// Smart pointer wrapper for the NFS context that handles cleanup on drop.
#[derive(Clone)]
struct NfsPtr(*mut sys::nfs_context);

impl Drop for NfsPtr {
    fn drop(&mut self) {
        if !self.0.is_null() {
            unsafe {
                sys::nfs_destroy_context(self.0);
            }
        }
    }
}

/// Check if a pointer is null and return an appropriate error.
fn check_mut_ptr<T>(ptr: *mut T) -> Result<*mut T> {
    if ptr.is_null() {
        ContextInitFailedSnafu.fail()
    } else {
        Ok(ptr)
    }
}

/// Check the return code from an NFS operation and convert errors.
fn check_retcode(ctx: *mut sys::nfs_context, code: i32) -> Result<()> {
    if code < 0 {
        unsafe {
            let err_str = sys::nfs_get_error(ctx);
            let message = if err_str.is_null() {
                // Defensive fallback: avoid undefined behavior if libnfs ever returns null
                "Unknown NFS error (libnfs returned null error string)".to_string()
            } else {
                CStr::from_ptr(err_str).to_string_lossy().into_owned()
            };
            NfsOperationFailedSnafu { message }.fail()
        }
    } else {
        Ok(())
    }
}

/// The main NFS client handle.
///
/// # Thread Safety
///
/// This type uses `Rc` for reference counting and is **not** thread-safe.
/// It does not implement `Send` or `Sync`. All operations on an `Nfs` handle
/// and its derived types (`NfsFile`, `NfsDirectory`) must occur on the same thread.
///
/// When using with async runtimes like Tokio, wrap NFS operations in
/// `tokio::task::spawn_blocking` to confine them to a single blocking thread.
#[derive(Clone)]
pub struct Nfs {
    context: Rc<NfsPtr>,
}

/// Represents the type of a directory entry.
#[derive(Clone, Debug, PartialEq, Eq)]
pub enum EntryType {
    Block,
    Character,
    Directory,
    File,
    NamedPipe,
    Symlink,
    Socket,
}

impl EntryType {
    fn from_ftype3(t: sys::ftype3) -> Result<Self> {
        match t {
            sys::ftype3_NF3BLK => Ok(Self::Block),
            sys::ftype3_NF3CHR => Ok(Self::Character),
            sys::ftype3_NF3DIR => Ok(Self::Directory),
            sys::ftype3_NF3REG => Ok(Self::File),
            sys::ftype3_NF3FIFO => Ok(Self::NamedPipe),
            sys::ftype3_NF3LNK => Ok(Self::Symlink),
            sys::ftype3_NF3SOCK => Ok(Self::Socket),
            _ => UnknownFileTypeSnafu { file_type: t }.fail(),
        }
    }
}

