//! Safe Rust bindings to libnfs C library for NFS client operations.
//!
//! This crate provides a safe wrapper around the libnfs C library, enabling
//! NFS client operations in Rust applications.
//!
//! # Example
//! ```ignore
//! use libnfs::{Nfs, EntryType};
//! use nix::fcntl::OFlag;
//! use std::path::Path;
//!
//! let mut nfs = Nfs::new()?;
//! nfs.mount("server.example.com", "/export")?;
//!
//! let dir = nfs.opendir(Path::new("/"))?;
//! for entry in dir {
//!     let entry = entry?;
//!     println!("{}: {:?}", entry.path.display(), entry.d_type);
//! }
//! ```

#![allow(non_upper_case_globals)]
#![allow(non_camel_case_types)]
#![allow(non_snake_case)]
#![allow(unsafe_code)]
// Allow unsafe extern blocks from bindgen-generated code
#![allow(unsafe_op_in_unsafe_fn)]

// libnfs bindings target 64-bit platforms; refuse to compile on narrower pointer widths.
#[cfg(not(target_pointer_width = "64"))]
compile_error!("libnfs bindings require a 64-bit target pointer width");

mod sys {
    //! Raw FFI bindings generated by bindgen.
    // Allow deprecated items from generated bindings
    #![allow(deprecated)]
    #![allow(dead_code)]
    #![allow(clippy::upper_case_acronyms)]
    #![allow(clippy::pub_underscore_fields)]
    #![allow(clippy::struct_field_names)]
    // Bindgen generates #[allow(...)] attributes which violate allow_attributes lint
    #![allow(clippy::allow_attributes)]
    // SAFETY: The bindings are generated by bindgen from the libnfs C headers.
    // All extern functions are inherently unsafe as they call into C code.
    // Bindgen currently generates `extern "C"` blocks that are compatible with
    // Rust 2021; under Rust 2024 these are treated equivalently to
    // `unsafe extern "C"`, and we only call them from within explicit `unsafe`
    // blocks in this crate's safe wrapper APIs.
    include!(concat!(env!("OUT_DIR"), "/bindings.rs"));
}

