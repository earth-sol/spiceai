    /// Mount an NFS share.
    ///
    /// # Arguments
    /// * `server` - The NFS server hostname or IP address
    /// * `export_name` - The export path on the server
    ///
    /// # Errors
    /// Returns an error if the mount operation fails.
    pub fn mount(&self, server: &str, export_name: &str) -> Result<()> {
        let server = CString::new(server.as_bytes()).context(InvalidPathSnafu)?;
        let export = CString::new(export_name.as_bytes()).context(InvalidPathSnafu)?;
        unsafe {
            check_retcode(
                self.context.0,
                sys::nfs_mount(self.context.0, server.as_ptr(), export.as_ptr()),
            )?;
            Ok(())
        }
    }

    /// Open a directory for iteration.
    ///
    /// # Errors
    /// Returns an error if the directory cannot be opened.
    pub fn opendir(&self, path: &Path) -> Result<NfsDirectory> {
        let path = CString::new(path.as_os_str().as_bytes()).context(InvalidPathSnafu)?;
        unsafe {
            let mut dir_handle: *mut sys::nfsdir = ptr::null_mut();
            check_retcode(
                self.context.0,
                sys::nfs_opendir(self.context.0, path.as_ptr(), &raw mut dir_handle),
            )?;
            Ok(NfsDirectory {
                nfs: Rc::clone(&self.context),
                handle: dir_handle,
            })
        }
    }

    /// Open a file with the specified flags.
    ///
    /// # Supported flags
    /// * `O_APPEND`
    /// * `O_RDONLY`
    /// * `O_WRONLY`
    /// * `O_RDWR`
    /// * `O_SYNC`
    /// * `O_TRUNC` (only valid with `O_RDWR` or `O_WRONLY`)
    ///
    /// # Errors
    /// Returns an error if the file cannot be opened.
    pub fn open(&self, path: &Path, flags: OFlag) -> Result<NfsFile> {
        let path = CString::new(path.as_os_str().as_bytes()).context(InvalidPathSnafu)?;
        unsafe {
            let mut file_handle: *mut sys::nfsfh = ptr::null_mut();
            check_retcode(
                self.context.0,
                sys::nfs_open(
                    self.context.0,
                    path.as_ptr(),
                    flags.bits(),
                    &raw mut file_handle,
                ),
            )?;
            Ok(NfsFile {
                nfs: Rc::clone(&self.context),
                handle: file_handle,
            })
        }
    }

    /// Create a new file with the specified mode.
    ///
    /// This is a thin wrapper around the `libnfs` `nfs_creat` call, which only
    /// accepts a POSIX mode and does not support `OFlag`-style open flags. If you
    /// need to control flags such as `O_TRUNC`, `O_EXCL`, or `O_SYNC`, use
    /// [`Nfs::open`] with the desired [`OFlag`]s instead of this method.
    ///
    /// # Errors
    /// Returns an error if the file cannot be created.
    pub fn create(&self, path: &Path, mode: Mode) -> Result<NfsFile> {
        let path = CString::new(path.as_os_str().as_bytes()).context(InvalidPathSnafu)?;
        unsafe {
            let mut file_handle: *mut sys::nfsfh = ptr::null_mut();
            // mode_t is u16 on macOS, u32 on Linux; convert to i32 for the C API
            #[cfg(target_os = "macos")]
            let mode_i32 = i32::from(mode.bits());
            #[cfg(not(target_os = "macos"))]
            #[expect(clippy::cast_possible_wrap)]
            let mode_i32 = mode.bits() as i32;
            check_retcode(
                self.context.0,
                sys::nfs_creat(
                    self.context.0,
                    path.as_ptr(),
                    mode_i32,
                    &raw mut file_handle,
                ),
            )?;
            Ok(NfsFile {
                nfs: Rc::clone(&self.context),
                handle: file_handle,
            })
        }
    }

