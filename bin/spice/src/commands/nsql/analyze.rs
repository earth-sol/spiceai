//! `spice nsql analyze` subcommand for analyzing Text-to-SQL performance.
//!
//! This module implements the `spice nsql analyze` subcommand which sends a natural
//! language query to the nsql endpoint and compares the generated SQL against an
//! expected SQL query, computing various metrics to evaluate the quality of the
//! text-to-SQL translation.
//!
//! # Metrics Computed
//!
//! Functional metrics (comparing generated vs expected SQL):
//! - `exact_match`: 1.0 if generated SQL exactly matches expected, 0.0 otherwise
//! - `correct_tables`: Intersection over Union (`IoU`) of tables referenced
//! - `correct_projections`: `IoU` of projected columns/expressions
//! - `correct_schema`: `IoU` of output schema fields
//!
//! Performance metrics (from `runtime.task_history`):
//! - `input_tokens`: Total prompt tokens used by LLM calls
//! - `output_tokens`: Total completion tokens generated by LLM
//! - `latency_ms`: End-to-end latency of the nsql request
//! - `sql_duration_ms`: Total time spent executing SQL queries
//! - `llm_duration_ms`: Total time spent in LLM inference
//! - `sql_query_count`: Number of SQL queries executed
//! - `llm_count`: Number of LLM completion calls made
//!
//! # Trace Correlation
//!
//! The command uses W3C Trace Context (traceparent header) to correlate the
//! nsql request with its task history entries. The `trace_id` is used to query
//! `runtime.task_history` for metrics after the request completes.

use crate::context::RuntimeContext;
use crate::error::{ConnectionFailedSnafu, InvalidResponseSnafu, Result};
use clap::Args;
use opentelemetry::trace::TraceId;
use rand::RngCore;
use serde::Serialize;
use snafu::ResultExt;
use spiceai::ClientBuilder;
use std::collections::HashSet;
use std::fmt::Write;
use std::time::Instant;
use test_framework::spicetest::text_to_sql::{
    find_task_history_metrics, intersection_over_union,
    parse::{attempt_parse_table_and_projection, logical_plan, sql_schema},
};

/// Arguments for the `nsql analyze` subcommand.
#[derive(Args, Debug)]
pub struct AnalyzeArgs {
    /// Natural language query to analyze
    #[arg(long)]
    pub query: String,

    /// Expected SQL query for comparison
    #[arg(long)]
    pub expected: String,

    /// Model to use for text-to-SQL (required)
    #[arg(long, short)]
    pub model: String,
}

/// Result of the nsql analyze operation.
#[derive(Debug, Serialize)]
pub struct AnalyzeResult {
    /// The natural language query that was analyzed.
    pub query: String,
    /// The expected SQL query provided for comparison.
    pub expected: String,
    /// The model used for text-to-SQL conversion.
    pub model: String,
    /// The actual SQL query generated by the nsql endpoint.
    pub actual: String,
    /// Error message if the analysis failed.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub error: Option<String>,
    /// Performance and functional metrics.
    pub metrics: AnalyzeMetrics,
    /// List of SQL queries attempted as candidate answers (after AI completion).
    pub attempted_sql: Vec<String>,
}

/// Metrics from the nsql analyze operation.
#[derive(Debug, Default, Serialize)]
pub struct AnalyzeMetrics {
    /// 1.0 if generated SQL exactly matches expected, 0.0 otherwise.
    pub exact_match: f64,
    /// Intersection over Union of tables referenced in the queries.
    pub correct_tables: f64,
    /// Intersection over Union of projected columns/expressions.
    pub correct_projections: f64,
    /// Intersection over Union of output schema fields.
    pub correct_schema: f64,
    /// Total prompt tokens used by LLM calls.
    pub input_tokens: u64,
    /// Total completion tokens generated by LLM.
    pub output_tokens: u64,
    /// End-to-end latency of the nsql request in milliseconds.
    pub latency_ms: f64,
    /// Total time spent executing SQL queries in milliseconds.
    pub sql_duration_ms: f64,
    /// Total time spent in LLM inference in milliseconds.
    pub llm_duration_ms: f64,
    /// Total number of SQL queries executed during the request.
    pub sql_query_count: usize,
    /// Number of LLM completion calls made during the request.
    pub llm_count: usize,
}

/// Request body for the nsql endpoint.
#[derive(Serialize)]
struct NsqlRequest {
    query: String,
    model: String,
}

/// Execute the `nsql analyze` subcommand.
///
/// # Errors
///
/// Returns an error if the API requests fail or output fails.
pub async fn execute(ctx: &RuntimeContext, args: &AnalyzeArgs) -> Result<()> {
    let result = run_analysis(ctx, &args.query, &args.expected, &args.model).await;

    let output = serde_json::to_string_pretty(&result).map_err(|e| {
        InvalidResponseSnafu {
            message: format!("Failed to serialize result: {e}"),
        }
        .build()
    })?;

    println!("{output}");
    Ok(())
}

/// Generate a random `TraceId`.
fn generate_trace_id() -> TraceId {
    let mut bytes = [0u8; 16];
    rand::rng().fill_bytes(&mut bytes);
    TraceId::from_bytes(bytes)
}

/// Format a W3C Trace Context traceparent header value.
fn format_traceparent(trace_id: &TraceId) -> String {
    let mut parent_id = [0u8; 8];
    rand::rng().fill_bytes(&mut parent_id);
    let mut parent_hex = String::with_capacity(16);
    for b in parent_id {
        let _ = write!(parent_hex, "{b:02x}");
    }
    format!("00-{trace_id}-{parent_hex}-01")
}

/// Build a spiceai client from the runtime context.
async fn build_spice_client(ctx: &RuntimeContext) -> Result<spiceai::Client> {
    let mut builder = ClientBuilder::new().http_url(ctx.http_endpoint());

    if let Some(api_key) = ctx.api_key() {
        builder = builder.api_key(api_key);
    }

    builder = builder.user_agent(ctx.user_agent());

    builder.build().await.map_err(|e| {
        InvalidResponseSnafu {
            message: format!("Failed to build spice client: {e}"),
        }
        .build()
    })
}

/// Perform the text-to-SQL analysis and return the result.
async fn run_analysis(
    ctx: &RuntimeContext,
    query: &str,
    expected: &str,
    model: &str,
) -> AnalyzeResult {
    let mut result = AnalyzeResult {
        query: query.to_string(),
        expected: expected.to_string(),
        model: model.to_string(),
        actual: String::new(),
        error: None,
        metrics: AnalyzeMetrics::default(),
        attempted_sql: Vec::new(),
    };

    // Build the spice client for task history queries
    let spice_client = match build_spice_client(ctx).await {
        Ok(client) => client,
        Err(e) => {
            result.error = Some(e.to_string());
            return result;
        }
    };

    // Generate a random trace ID for correlating task history
    let trace_id = generate_trace_id();
    let traceparent = format_traceparent(&trace_id);

    tracing::debug!(trace_id = %trace_id, traceparent = %traceparent, "using trace ID for nsql analyze");

    // Send the nsql request
    let start = Instant::now();
    let generated_sql = match send_nsql_request_with_trace(ctx, query, model, &traceparent).await {
        Ok(sql) => sql,
        Err(e) => {
            result.error = Some(e.to_string());
            result.metrics.latency_ms = start.elapsed().as_secs_f64() * 1000.0;
            return result;
        }
    };
    let latency_ms = start.elapsed().as_secs_f64() * 1000.0;

    result.actual.clone_from(&generated_sql);
    result.metrics.latency_ms = latency_ms;

    // Fetch task history metrics using test-framework function
    match find_task_history_metrics(&spice_client, &trace_id).await {
        Ok((gen_sql, th_metrics)) => {
            result.metrics.sql_duration_ms = th_metrics.sql_duration_ms;
            result.metrics.sql_query_count = th_metrics.sql_count;
            result.metrics.llm_duration_ms = th_metrics.llm_duration_ms;
            result.metrics.llm_count = th_metrics.llm_count;
            result.metrics.input_tokens = th_metrics.llm_input_tokens;
            result.metrics.output_tokens = th_metrics.llm_output_tokens;
            result.attempted_sql = th_metrics.attempted_sql;

            // Use task_history SQL if we didn't get it from response
            if result.actual.is_empty()
                && let Some(sql) = gen_sql
            {
                result.actual = sql;
            }
        }
        Err(e) => {
            tracing::warn!("failed to query task_history metrics: {e}");
        }
    }

    // When returning SQL only (no sql_query tasks after ai_completion),
    // use the returned SQL as the attempted SQL
    if result.attempted_sql.is_empty() && !result.actual.is_empty() {
        result.attempted_sql = vec![result.actual.clone()];
    }

    // Compute functional metrics
    result.metrics.exact_match = compute_exact_match(&result.actual, expected);

    // Get logical plans and compute structural metrics using test-framework functions
    let http_client = ctx.http_client().clone();
    let http_base_url = ctx.http_endpoint();

    let expected_plan = logical_plan(http_client.clone(), http_base_url, expected).await;
    let generated_plan = logical_plan(http_client.clone(), http_base_url, &result.actual).await;

    if let (Ok(exp_plan), Ok(gen_plan)) = (&expected_plan, &generated_plan) {
        let (expected_tables, expected_projections) =
            test_framework::spicetest::text_to_sql::parse::extract_tables_and_projection(exp_plan);
        let (generated_tables, generated_projections) =
            test_framework::spicetest::text_to_sql::parse::extract_tables_and_projection(gen_plan);

        result.metrics.correct_tables =
            intersection_over_union(&expected_tables, &generated_tables);
        result.metrics.correct_projections =
            intersection_over_union(&expected_projections, &generated_projections);
    } else if let Ok((exp_tables, exp_proj)) = attempt_parse_table_and_projection(expected)
        && let Ok((gen_tables, gen_proj)) = attempt_parse_table_and_projection(&result.actual)
    {
        // Fallback to SQL parsing if logical plan fails
        result.metrics.correct_tables = intersection_over_union(&exp_tables, &gen_tables);
        result.metrics.correct_projections = intersection_over_union(&exp_proj, &gen_proj);
    }

    // Compute schema similarity using test-framework function
    let expected_schema = sql_schema(http_client.clone(), http_base_url, expected).await;
    let generated_schema = sql_schema(http_client, http_base_url, &result.actual).await;

    if let (Ok(exp_schema), Ok(gen_schema)) = (&expected_schema, &generated_schema) {
        let exp_fields: HashSet<String> = exp_schema
            .fields()
            .iter()
            .map(|f| format!("{}:{}", f.name(), f.data_type()))
            .collect();
        let gen_fields: HashSet<String> = gen_schema
            .fields()
            .iter()
            .map(|f| format!("{}:{}", f.name(), f.data_type()))
            .collect();
        result.metrics.correct_schema = intersection_over_union(&exp_fields, &gen_fields);
    }

    result
}

/// Send a text-to-SQL request with a traceparent header for trace correlation.
async fn send_nsql_request_with_trace(
    ctx: &RuntimeContext,
    query: &str,
    model: &str,
    traceparent: &str,
) -> Result<String> {
    let url = format!("{}/v1/nsql", ctx.http_endpoint());

    let body = NsqlRequest {
        query: query.to_string(),
        model: model.to_string(),
    };

    let mut request = ctx
        .http_client()
        .post(&url)
        .header("Content-Type", "application/json")
        .header("Accept", "application/sql")
        .header("traceparent", traceparent)
        .json(&body);

    for (key, value) in ctx.get_headers() {
        request = request.header(&key, &value);
    }

    let response = request
        .send()
        .await
        .context(ConnectionFailedSnafu { endpoint: &url })?;

    let status = response.status();
    let text = response.text().await.unwrap_or_default();

    if !status.is_success() {
        return Err(InvalidResponseSnafu {
            message: format!("nsql request failed with status {status}: {text}"),
        }
        .build());
    }

    Ok(text.trim().to_string())
}

/// Compute exact match: 1.0 if actual matches expected (after trimming), 0.0 otherwise.
fn compute_exact_match(actual: &str, expected: &str) -> f64 {
    if actual.trim() == expected.trim() {
        1.0
    } else {
        0.0
    }
}
